fun main(args: Array<String>) {
    val numero1 = 11
    val numero2 = 5

    val resultadoSoma = numero1 + numero2
    val resultadoSubtracao = numero1 - numero2
    val resultadoMultiplicacao = numero1 * numero2
    val resultadoDivisao = numero1 * numero2
    val resultadoRestoDaDivisao = numero1 * numero2

    println(resultadoSoma)
    println(resultadoSubtracao)
    println(resultadoMultiplicacao)
    println(resultadoDivisao)
    println(resultadoRestoDaDivisao)
}